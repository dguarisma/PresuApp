// Función para obtener los idiomas disponibles
export function getAvailableLanguages(): Language[] {
  return Object.keys(translations) as Language[]
}

// Función para obtener una traducción
export function getTranslation(lang: Language, key: string, replacements?: Record<string, string | number>): string {
  // Dividir la clave por puntos para acceder a objetos anidados
  const keys = key.split(".")
  let translation: any = translations[lang]

  // Navegar por el objeto de traducciones
  for (const k of keys) {
    if (translation && translation[k] !== undefined) {
      translation = translation[k]
    } else {
      // Si no se encuentra la traducción, devolver la clave
      return key
    }
  }

  // Si la traducción no es una cadena, devolver la clave
  if (typeof translation !== "string") {
    return key
  }

  // Reemplazar variables si existen
  if (replacements) {
    Object.entries(replacements).forEach(([k, v]) => {
      translation = translation.replace(new RegExp(`{{${k}}}`, "g"), String(v))
    })
  }

  return translation
}
